export function SocialLinks({ business, variant = 'icons', className = '' }: SocialLinksProps) {
  const active = PLATFORMS.filter(p => !!(business as any)[p.key]);

  if (active.length === 0) return null;

  if (variant === 'icons') {
    return (
      <div className={`flex flex-wrap gap-2 ${className}`}>
        {active.map(p => {
          const value = (business as any)[p.key] as string;
          return (
            <a
              key={p.key}
              href={p.getHref(value)}
              target="_blank"
              rel="noopener noreferrer"
              title={p.label}
              className="inline-flex items-center justify-center w-8 h-8 rounded-full transition-opacity hover:opacity-80 focus:outline-none focus:ring-2 focus:ring-offset-1 focus:ring-gray-400"
              style={{ backgroundColor: p.color, color: (p as any).iconBg ?? '#fff' }}
              aria-label={p.label}
            >
              {p.icon}
            </a>
          );
        })}
      </div>
    );
  }

  // list variant
  return (
    <div className={`space-y-2 ${className}`}>
      {active.map(p => {
        const value = (business as any)[p.key] as string;
        return (
          <div key={p.key} className="flex items-start gap-2">
            <span
              className="inline-flex items-center justify-center w-6 h-6 rounded-full shrink-0 mt-0.5"
              style={{ backgroundColor: p.color, color: (p as any).iconBg ?? '#fff' }}
            >
              {p.icon}
            </span>
            <div className="min-w-0">
              <p className="text-xs text-gray-500 dark:text-gray-400">{p.label}</p>
              <a
                href={p.getHref(value)}
                target="_blank"
                rel="noopener noreferrer"
                className="text-sm text-blue-600 hover:underline break-all"
              >
                {value}
              </a>
            </div>
          </div>
        );
      })}
    </div>
  );
}
